    private void cmd_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_updateActionPerformed
        // TODO add your handling code here:
        int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to update record?","Update Record",JOptionPane.YES_NO_OPTION);
        if(p==0){
            
            if (txt_username.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "Debes ingresar un nombre de usuario");
                txt_username.requestFocus();
                return;
            }
            
            if (txt_password.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "Debes ingresar una contraseña");
                txt_password.requestFocus();
                return;
            }
            
         try{
            
            String value1 = txt_id.getText();
            String value2 = txt_username.getText();
            String value3 = txt_password.getText();
            String value4 = String.valueOf(cmb_perfil.getSelectedItem());
            
            String sql= "update Users set username='"+value2+"', password='"+value3+"',perfil='"+value4+"' where id='"+value1+"' ";
            
            pst=conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Registro Editado");
            
        }catch(Exception e){
       JOptionPane.showMessageDialog(null, e);
        }
        finally {
            
            try{
                rs.close();
                pst.close();
                
            }
            catch(Exception e){
                
            }
        }
        }
//        Fillcombobox();
    }//GEN-LAST:event_cmd_updateActionPerformed


    private void cmd_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_addActionPerformed
        int p = JOptionPane.showConfirmDialog(null, "Realmente desea adicionar el registro?","Registro Adicionado",JOptionPane.YES_NO_OPTION);
             if(p==0){        // TODO add your handling code here:
                 
                 if (txt_username.getText().length() == 0) {
                    JOptionPane.showMessageDialog(null, "Debes ingresar un nombre de usuario");
                    txt_username.requestFocus();
                    return;
                }
            
                if (txt_password.getText().length() == 0) {
                    JOptionPane.showMessageDialog(null, "Debes ingresar una contraseña");
                    txt_password.requestFocus();
                    return;
                }
                
                General gn = new General();
                int login = gn.userRepeat(txt_username.getText());
                if (login == 1) {
                    JOptionPane.showMessageDialog(null, "Este usuario : " + txt_username.getText() + " ya esta ocupado por otro usuario.");
                    txt_username.setText("");
                    txt_username.requestFocus();
                    return;
                }

             try {

                 String sql ="insert into Users (username,password,perfil) values (?,?,?) ";

                 pst=conn.prepareStatement(sql);
                 pst.setString(1,txt_username.getText());
                 pst.setString(2,txt_password.getText());
                 pst.setString(3,String.valueOf(cmb_perfil.getSelectedItem()));

                 pst.execute();
                 JOptionPane.showMessageDialog(null,"Datos gravados con exito");

             }
             catch (Exception e)

             {
                 JOptionPane.showMessageDialog(null,e);
             }
             finally {

                 try{
                     rs.close();
                     pst.close();

                 }
                 catch(Exception e){

                 }
             }
             } 
//             Fillcombobox();
    }//GEN-LAST:event_cmd_addActionPerformed

